<h3>What can CU3ER be used for?</h3>

<p>CU3ER \kju:bər\, an image slider initially conceived to create 3D transitions between slides, turned out to be a convenient and multifunction solution that can be applied in a range of website building areas, from content slider to feature slider and image & banner rotator. Consider using it when you want to grab the user’s attention, and you’ll be delighted by the results!</p>

<p>If you’d like to experience more creativity in web development, are striving to more visually appealing content, and prefer to have even more unique image transitions on your websites, try the CU3ER! Its magic is astonishing!</p>

<h3>Not always the best solution!</h3>

<p>To be fair, it must be admitted, the CU3ER is not always the best solution. In some cases, there are more appropriate answers to your needs. The user always comes first! Therefore, identify your target audience, analyze the content people are expecting to find, recognize the potential effects of the solution you’d like to implement! Once you’ve experienced the magic of CU3ER, you’ll hardly want to go back to anything known before!</p> 

<h3>Requirements</h3>

<p>There are no special requirements for setting it up and running other than a limited knowledge of web authoring:</p>
<ul id="attribute-list" class="nobullet"> 
